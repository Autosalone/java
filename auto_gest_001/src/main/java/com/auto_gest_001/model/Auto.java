package com.auto_gest_001.model;


public class Auto {
	
	private int idAuto = 0;
	private String casaProduttrice = "";
	private String modello = "";
	private boolean prenotata = false;
	
		
	public int getIdAuto() {
		return idAuto;
	}
	public void setIdAuto(int idAuto) {
		this.idAuto = idAuto;
	}
	public String getCasaProduttrice() {
		return casaProduttrice;
	}
	public void setCasaProduttrice(String casaProduttrice) {
		this.casaProduttrice = casaProduttrice;
	}
	public String getModello() {
		return modello;
	}
	public void setModello(String modello) {
		this.modello = modello;
	}
	public boolean isPrenotata() {
		return prenotata;
	}
	public void setPrenotata(boolean prenotata) {
		this.prenotata = prenotata;
	}
	
	
	

	
	
	
	
	
	
}
